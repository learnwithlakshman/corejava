package com.careerit.cj.day1;

public class SumOfTwoNumbers {

		public static void main(String[] args) {
			int num1 = 10;
			int num2 = 20;
			int sum = num1 + num2;
			int sub = num1 - num2;
			System.out.print("The sum of "+num1+" + "+num2 +" = "+sum);
			System.out.print("The sub of "+num1+" - "+num2 +" = "+sub);
			
			
		}
}
